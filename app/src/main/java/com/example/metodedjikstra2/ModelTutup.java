package com.example.metodedjikstra2;

public class ModelTutup {
    private String data;
    private String line;

    public ModelTutup() {
        super();
    }

    public ModelTutup(String data, String line) {
        this.data = data;
        this.line = line;
    }

    public String getData() {
        return data;
    }

    public String getLine() {
        return line;
    }
}
